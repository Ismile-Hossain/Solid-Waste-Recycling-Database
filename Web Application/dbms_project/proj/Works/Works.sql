CREATE TABLE WORKS
(
	COMPANY_TRADE_NUM	VARCHAR2(20),
	EMPLOYEE_ID		 	VARCHAR2(15),
	
	CONSTRAINT WR_cmp_trdNum_eid_pk
		PRIMARY KEY (COMPANY_TRADE_NUM,EMPLOYEE_ID),
	CONSTRAINT WR_comp_trd_num_fk
		FOREIGN KEY (COMPANY_TRADE_NUM)
		REFERENCES COMPANY (COMPANY_TRADE_NUM),
	CONSTRAINT WR_employee_id_fk
		FOREIGN KEY (employee_id)
		REFERENCES Employee (employee_id)
);


