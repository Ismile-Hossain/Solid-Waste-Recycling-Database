CREATE TABLE COMPANY
(
	COMPANY_TRADE_NUM VARCHAR2(15),
	NAME              VARCHAR2(40) NOT NULL,
	COLLECTION        NUMBER(20,5) CHECK (COLLECTION>0),
	RECYCLED_AMOUNT   NUMBER(20,5) CHECK (RECYCLED_AMOUNT>0),
	EXPENSE           NUMBER(20,5) CHECK (EXPENSE>0),
	RECYCLING_PERCENT NUMBER(20,5) CHECK (RECYCLING_PERCENT>0),
	
	CONSTRAINT COM_trade_num_pk
		PRIMARY KEY (COMPANY_TRADE_NUM)
);


