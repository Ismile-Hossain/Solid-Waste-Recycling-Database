CREATE TABLE PRODUCTION
(
	COMPANY_TRADE_NUM 	VARCHAR2(15),
	PRODUCT_ID 			VARCHAR2(15),
	PRODUCT_DATE     	DATE,
	
	CONSTRAINT P_trNum_pId_date_pk
		PRIMARY KEY (COMPANY_TRADE_NUM,PRODUCT_ID,PRODUCT_DATE),
	CONSTRAINT P_trNum_fk
		FOREIGN KEY (COMPANY_TRADE_NUM)
		REFERENCES COMPANY (COMPANY_TRADE_NUM),
	CONSTRAINT P_prod_id_date_fk
		FOREIGN KEY (PRODUCT_ID,PRODUCT_DATE)
		REFERENCES PRODUCT (PRODUCT_ID,PRODUCT_DATE)
);


