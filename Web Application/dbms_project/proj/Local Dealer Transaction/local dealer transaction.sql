CREATE TABLE  LOCAL_DEALER_TRANSACTION
(
	dealer_dealership_no 	VARCHAR2(15),
	TRANSACTION_ID 			VARCHAR2(20),
	TRANSACTION_DATE  		DATE,
	
	CONSTRAINT LDTS_dealNo_tsId_date_pk
		PRIMARY KEY (dealer_dealership_no,TRANSACTION_ID,TRANSACTION_DATE),
	CONSTRAINT LDTS_dealNo_fk
		FOREIGN KEY (dealer_dealership_no) 
		REFERENCES Local_Dealer	(dealer_dealership_no),
	CONSTRAINT LTDS_trans_id_date_fk
		FOREIGN KEY (TRANSACTION_ID,TRANSACTION_DATE)
		REFERENCES TRANSACTION_SUPPORT (TRANSACTION_ID,TRANSACTION_DATE)

);


