CREATE TABLE Local_Waste_Collector(
	Collector_ID 				 VARCHAR2(20),
	Collector_name				 VARCHAR2(25),	
	Collection_area 			 VARCHAR2(20),
	Collector_type_of_waste   	 VARCHAR2(15),
	Collector_waste_collected	 NUMBER(20,5) CHECK (Collector_waste_collected>0),
	Collecting_Date				 DATE,
	Collector_rate_of_waste      NUMBER(20,5) CHECK (Collector_rate_of_waste>0),
	Collector_income			 NUMBER(20,5) CHECK (Collector_income>0),
	Collector_Contact_No 		 VARCHAR2(11),
  Collector_type  VARCHAR2(20),
	CONSTRAINT LWC_Collector_ID_pk
		PRIMARY KEY (Collector_ID)	
);



