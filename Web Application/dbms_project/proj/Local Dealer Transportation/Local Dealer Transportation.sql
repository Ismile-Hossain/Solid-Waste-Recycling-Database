CREATE TABLE  LOCAL_DEALER_TRANSPORTATION
(
	dealer_dealership_no 	VARCHAR2(10),
	VEHICLE_NUMBER 			VARCHAR2(20),
    HIRER_ID        		VARCHAR2(20),
	
	CONSTRAINT LDT_dealNo_vechNum_pk 
		PRIMARY KEY (dealer_dealership_no,VEHICLE_NUMBER),
	CONSTRAINT LDT_dealNo_fk
		FOREIGN KEY (dealer_dealership_no) 
		REFERENCES Local_Dealer	(dealer_dealership_no),
	CONSTRAINT LDT_vehNum_hirId_fk
		FOREIGN KEY (VEHICLE_NUMBER,HIRER_ID)
		REFERENCES TRANSPORTATION (VEHICLE_NUMBER,HIRER_ID)
);

