CREATE TABLE PRODUCT
(
	PRODUCT_ID 			VARCHAR2(15),
	PRODUCT_DATE 		DATE,
	PRODUCT_NAME      	VARCHAR2(15),
	PRODUCT_TYPE        VARCHAR2(10),
	EXPIRY_DATE    		DATE,
	PRICE           	NUMBER(20,5) CHECK (PRICE>0),
   
	CONSTRAINT PROD_id_date_pk
		PRIMARY KEY (PRODUCT_ID,PRODUCT_DATE)
);


