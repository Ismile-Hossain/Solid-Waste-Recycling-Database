CREATE TABLE CLIENTS_TRANSACTION
(
	CLIENT_TRADE_NUM 		VARCHAR2(15),
	TRANSACTION_ID 			VARCHAR2(20),
	TRANSACTION_DATE  		DATE,
	
	CONSTRAINT CLIT_trdNum_tsIdDate_pk
		PRIMARY KEY (CLIENT_TRADE_NUM,TRANSACTION_ID,TRANSACTION_DATE),
	CONSTRAINT CLIT_cli_trd_num_fk
		FOREIGN KEY (CLIENT_TRADE_NUM)
		REFERENCES CLIENTS (CLIENT_TRADE_NUM),
	CONSTRAINT CLIT_trans_id_date_fk
		FOREIGN KEY (TRANSACTION_ID,TRANSACTION_DATE)
		REFERENCES TRANSACTION_SUPPORT (TRANSACTION_ID,TRANSACTION_DATE)

);

