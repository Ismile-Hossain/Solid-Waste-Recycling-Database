CREATE TABLE CLIENTS_TRANSPORTATION
(
	CLIENT_TRADE_NUM 	VARCHAR2(15),
	VEHICLE_NUMBER   	VARCHAR2(20),
    HIRER_ID        	VARCHAR2(20),
	
	CONSTRAINT CLITP_trdNum_vehNum_pk
		PRIMARY KEY (CLIENT_TRADE_NUM,VEHICLE_NUMBER,HIRER_ID),	
	CONSTRAINT CLITP_trade_Num_fk
		FOREIGN KEY (CLIENT_TRADE_NUM)
		REFERENCES CLIENTS (CLIENT_TRADE_NUM),
	CONSTRAINT CLITP_vehNum_hirId_fk
		FOREIGN KEY (VEHICLE_NUMBER,HIRER_ID)
		REFERENCES TRANSPORTATION (VEHICLE_NUMBER,HIRER_ID)
	   
);


