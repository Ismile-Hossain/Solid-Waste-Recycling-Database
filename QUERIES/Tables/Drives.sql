CREATE TABLE DRIVES
(
	VEHICLE_NUMBER 			VARCHAR2(20),
    HIRER_ID        		VARCHAR2(20),
	DRIVER_LICENSE_NUM 		VARCHAR2(20),
	
	CONSTRAINT DRS_vehNum_licNum_pk
		PRIMARY KEY (VEHICLE_NUMBER,HIRER_ID,DRIVER_LICENSE_NUM),
	CONSTRAINT DRS_vehNum_hirId_fk
		FOREIGN KEY (VEHICLE_NUMBER,HIRER_ID)
		REFERENCES TRANSPORTATION (VEHICLE_NUMBER,HIRER_ID),
	CONSTRAINT DRS_license_num_fk
		FOREIGN KEY (DRIVER_LICENSE_NUM)
		REFERENCES DRIVER (DRIVER_LICENSE_NUM)
);

