CREATE TABLE CLIENTS
(
	CLIENT_TRADE_NUM 		 VARCHAR2(15),
	CLIENTS_COMPANY_NAME 	 VARCHAR2(50),
	AMOUNT           		 NUMBER(20,5) CHECK (AMOUNT>0),
	RATE_PER_UNIT    		 NUMBER(20,5) CHECK (RATE_PER_UNIT>0),
	EXPENSE         		 NUMBER(20,5) CHECK (EXPENSE>0),
	
	CONSTRAINT CLI_trade_num_pk
		PRIMARY KEY (CLIENT_TRADE_NUM)
);


