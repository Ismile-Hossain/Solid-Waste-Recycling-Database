28. /* Recycling_Process */

SELECT process_id "PROCESS ID", process_date "PROCESSED DATE",CONCAT(process_collected_waste, ' kg') "COLLECTED WASTE",
		CONCAT(process_recycled_amount, ' kg') "RECYCLED AMOUNT",CONCAT(CAST((process_recycled_amount/process_collected_waste)*100 AS DECIMAL(20,2)), '%') "RECYCLING RATE"
FROM Recycling_Process;


