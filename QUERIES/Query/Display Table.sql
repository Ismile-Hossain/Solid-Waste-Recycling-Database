FROM LOCAL_DEALER_TRANSACTION;



18. /* LOCAL_DEALER_TRANSPORTATION */

SELECT dealer_dealership_no "DEALERSHIP NO.", VEHICLE_NUMBER "VEHICLE NO.", HIRER_ID "HIRER ID"
FROM LOCAL_DEALER_TRANSPORTATION; 



19. /* Local_Dealer */

SELECT dealer_dealership_no "DEALERSHIP NO.", dealer_name "NAME", dealer_city "CITY", dealer_district "DISTRICT"
FROM Local_Dealer;


20. /* Local_Waste_Collector */

SELECT Collector_ID "COLLECTOR ID", Collector_name "NAME", Collection_aera "AREA", Collector_type_of_waste "TYPE OF WASTE", CONCAT(Collector_waste_collected, ' kg') "COLLECTED AMOUNT",
		CONCAT(Collector_rate_of_waste,' tk') "PER UNIT PRICE",  CONCAT(Collector_income,' tk') "INCOME",  Collecting_Date "DATE", Collector_Contact_No "CONTACT NO."
FROM Local_Waste_Collector;

21. /* SCAVENGERS */

SELECT Collector_ID "COLLECTOR ID", Collector_name "NAME", Collection_aera "AREA", Collector_type_of_waste "TYPE OF WASTE",DUSTBIN_NUMBER "DUSTBIN NO.", CONCAT(Collector_waste_collected, ' kg') "COLLECTED AMOUNT", Collecting_Date "DATE",
		CONCAT(Collector_rate_of_waste,' tk') "PER UNIT PRICE", CONCAT(Collector_income,' tk') "INCOME", Collector_Contact_No "CONTACT NO."
FROM SCAVENGERS JOIN Local_Waste_Collector USING (Collector_ID);


22. /* WASTE_PICKER */

SELECT Collector_ID "COLLECTOR ID", Collector_name "NAME", Collection_aera "AREA", Collector_type_of_waste "TYPE OF WASTE", PICKING_HOUSE_NUMBER "HOUSE NO.", PICKING_ROAD_NUMBER "ROAD NO.",
		 Collector_waste_collected "COLLECTED AMOUNT", Collecting_Date "DATE", CONCAT(Collector_rate_of_waste,' tk') "PER UNIT PRICE",  CONCAT(Collector_income,' tk') "INCOME", Collector_Contact_No "CONTACT NO."
FROM WASTE_PICKER JOIN Local_Waste_Collector USING (Collector_ID);



22. /* INSTITUTION_WORKER */

SELECT Collector_ID "COLLECTOR ID", Collector_name "NAME", Collection_aera "AREA", Collector_type_of_waste "TYPE OF WASTE", INSTITUTION_NAME "INSTITUTION NAME", INSTITUTION_TYPE "INSTITUTION TYPE",
		Collector_waste_collected "COLLECTED AMOUNT", Collecting_Date "DATE", CONCAT(Collector_rate_of_waste,' tk') "PER UNIT PRICE",  CONCAT(Collector_income,' tk') "INCOME", Collector_Contact_No "CONTACT NO."
FROM INSTITUTION_WORKER JOIN Local_Waste_Collector USING (Collector_ID);



23. /* COMMUNITY_WORKER */

SELECT Collector_ID "COLLECTOR ID", Collector_name "NAME", Collection_aera "AREA", COMMUNITY_NAME "COMMUNITY NAME", COMMUNITY_HEAD "COMMUNITY HEAD NAME", COMMUNITY_HEAD_CONTACT_NUM "CONTACT NO.", COMMUNITY_WASTE_COLLECTOR_NUM "WASTE COLLECTOR NO.", 
		Collector_type_of_waste "TYPE OF WASTE", Collector_waste_collected "COLLECTED AMOUNT", Collecting_Date "DATE", CONCAT(Collector_rate_of_waste,' tk') "PER UNIT PRICE",  CONCAT(Collector_income,' tk') "INCOME", Collector_Contact_No "CONTACT NO."
FROM COMMUNITY_WORKER JOIN Local_Waste_Collector USING (Collector_ID);



