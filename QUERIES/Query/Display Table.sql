1. /* AGENT TRANSACTION */

SELECT AGENT_ID "AREA AGENT ID", TRANSACTION_ID "TRANSACTION ID", TRANSACTION_DATE "TRANSACTION DATE"
FROM AGENT_TRANSACTION; 


2. /* AGGREGATOR */ 

SELECT AGENT_ID "AREA AGENT ID", Collector_ID "WASTE COLLECTOR ID"
FROM AGGREGATOR;


3. /* AGENT TRANSPORTATION */

SELECT AGENT_ID "AREA AGENT ID", VEHICLE_NUMBER "VEHICLE NO.", HIRER_ID "HIRER ID"
FROM AGENT_TRANSPORTATION;


4. /* Area Agent */

SELECT agent_id "AREA AGENT ID", agent_name "NAME", agent_city "CITY", 
		agent_area "AREA", agent_shop_name "SHOP NAME",  agent_shop_open_time "SHOP OPENNING TIME",
		agent_shop_close_time "SHOP CLOSING TIME"
FROM Area_Agent;


5. /* Area_Agent_Contact_No */

SELECT agent_id "AREA AGENT ID", agent_name "NAME", agent_phone_no "CONTACT NO"
FROM Area_Agent_Contact_No JOIN Area_Agent USING (agent_id);



6. /* CLIENTS TRANSACTION */

SELECT CLIENT_TRADE_NUM "CLINET TRADE NO.", TRANSACTION_ID "TRANSACTION ID", TRANSACTION_DATE "TRANSACTION DATE"
FROM CLIENTS_TRANSACTION;



7. /* CLIENTS TRANSPORTATION */

SELECT CLIENT_TRADE_NUM "CLINET TRADE NO.",VEHICLE_NUMBER "VEHICLE NO.", HIRER_ID "HIRER ID"
FROM CLIENTS_TRANSPORTATION; 



8. /* CLIENTS */

SELECT CLIENT_TRADE_NUM "CLINET TRADE NO.", CLIENTS_COMPANY_NAME "COMPANY NAME"
FROM CLIENTS;



9. /* COLLECTOR */

SELECT AGENT_ID "AREA AGENT ID", dealer_dealership_no "DEALERSHIP NO."
FROM COLLECTOR;



10. /* COMPANY_TRANSACTION */ 

SELECT COMPANY_TRADE_NUM "COMPANY TRADE NO.", TRANSACTION_ID "TRANSACTION ID", TRANSACTION_DATE "TRANSACTION DATE"
FROM COMPANY_TRANSACTION;




11. /* COMPANY_TRANSPORTATION */

SELECT COMPANY_TRADE_NUM "COMPANY TRADE NO.", VEHICLE_NUMBER "VEHICLE NO.", HIRER_ID "HIRER ID"
FROM COMPANY_TRANSPORTATION;




12. /* COMPANY */

SELECT COMPANY_TRADE_NUM "COMPANY TRADE NO.", NAME, CONCAT(COLLECTION, ' kg') "COLLECTED WASTE", CONCAT(RECYCLED_AMOUNT, ' kg') "RECYCLED AMOUNT",
		CONCAT(EXPENSE, ' tk') "EXPENSE", CONCAT(CAST((RECYCLED_AMOUNT/COLLECTION)*100 AS DECIMAL(5,2)), '%') "PERCENTAGE OF RECYCLING"
FROM COMPANY;



13. /* DRIVER */

SELECT DRIVER_LICENSE_NUM "LICENSE NO.",  DRIVER_NAME "NAME", DRIVER_ADDRESS "ADDRESS", DRIVER_VEHICLE_TYPE "TYPE OF VEHICLE"
FROM DRIVER;



14. /* DRIVES */

SELECT VEHICLE_NUMBER "VEHICLE NO.", HIRER_ID "HIRER ID",DRIVER_LICENSE_NUM "DRIVER LICENSE NO."
FROM DRIVES;



15. /* Employee */

SELECT employee_id "EMPLOYEE ID", employee_name "NAME", employee_designation "DESIGNATION", CONCAT(employee_salary,' tk') "SALARY",
		employee_email "EMAIL", employee_phone_no "CONTACT NO."
FROM Employee JOIN Employee_Contact_No USING (employee_id);



16. /* ERROR */ 

SELECT process_id "PROCESS ID",process_date "DATE", error_type "ERROR TYPE", CONCAT(error_solving_cost,' tk') "SOLVING COST",
		employee_id "SUPERVISOR ID"
FROM Error;


17. /* LOCAL_DEALER_TRANSACTION */ 

